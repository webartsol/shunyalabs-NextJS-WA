// Get popular languages (top 20 by native speakers)
export const getPopularLanguages = (): LanguageOption[] => {
  return languageOptions
    .filter((lang) => lang.status === "Available")
    .sort((a, b) => b.nativeSpeakers - a.nativeSpeakers)
    .slice(0, 20);
};

// Get all regions
export const getRegions = (): string[] => {
  return [...new Set(languageOptions.map((lang) => lang.region))].sort();
};
