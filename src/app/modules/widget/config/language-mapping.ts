export const getDirectLanguages = () => {
  return direct_languages.map((name) => {
    const langDetail = languageOptions.find(
      (l) => l.name === name || l.name.startsWith(name),
    );
    return {
      name,
      code: langDetail ? langDetail.code : name,
      flagCode: directLanguageFlags[name] || langDetail?.flagCode || "🌐",
      flag:
        langDetail?.flag ||
        (directLanguageFlags[name] === "🇮🇳"
          ? "/data/flags/4x3/in.svg"
          : "/data/flags/4x3/un.svg"),
      region: "Direct",
      status: "Available" as const,
      nativeSpeakers: 0,
    };
  });
};

export const getLanguageNameByCode = (code: string): string => {
  if (code === "hi-en" || code === "med-en") return code;
  if (direct_languages.includes(code)) return code;
  const lang = languageOptions.find((l) => l.code === code);
  return lang ? lang.name : code;
};

